    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String userTxt = this.jTextField1.getText();
        if(userTxt==null||userTxt.isEmpty()) {
            this.cleanFields();
            this.jTextPane1.setText(TEXT_NO_VALID_INPUT);
            return;
        }
        
        try {
            Result res= procUser.checkUsername(userTxt);
            if(res!=null&&!res.isExistingUsrname()) {
                this.jTextPane1.setText(VALID_USER);
            } else {
                StringBuilder txt = new StringBuilder();
                txt.append(NO_VALID_USER);
                if(res!=null) {
                    String[] suges=res.getSuggestions();
                    for(int i=0;i<suges.length;i++)  {
                        txt.append(i+1);
                        txt.append(": ");
                        txt.append(suges[i]);
                        txt.append("\n");
                    }
                    this.jTextPane1.setText(txt.toString());
                } else {
                    this.jTextPane1.setText(ERROR_TXT);
                }
                
            }
        } catch (RestrictedWordException ex) {
            this.jTextPane1.setText(RESTRICTED_WORD+ex.getMessage());
        } catch (UsernameLengthException ex) {
            this.jTextPane1.setText(TEXT_NO_VALID_INPUT);
        } catch(Exception ex) {
            this.jTextPane1.setText(ERROR_TXT);
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

