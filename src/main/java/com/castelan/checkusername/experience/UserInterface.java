    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        String restrictedTxt = this.jTextField2.getText();
        if(restrictedTxt==null||restrictedTxt.isEmpty()) {
            this.cleanFields();
            this.jTextPane1.setText(TEXT_NO_VALID__RES_INPUT);
            return;
        }
        try {
            procUser.addRestrictedWord(restrictedTxt);
            this.jTextPane1.setText(RES_SUCCESS);
        } catch(Exception ex) {
            this.cleanFields();
            this.jTextPane1.setText(ERROR_TXT);
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton4MouseClicked

